import { Devs } from "@utils/constants";
import definePlugin from "@utils/types";

const STYLE_ID = "swancord-hide-avatar-decoration";

// Hides Nitro avatar decorations (the animated frames around profile pictures)
const CSS = `
[class*="avatarDecoration_"],
[class*="decoration_"][class*="avatar"],
[class*="avatarDecorationMask_"] {
    display: none !important;
}
`;

export default definePlugin({
    name: "HideAvatarDecoration",
    description: "Hides Nitro avatar decorations (the animated frames around profile pictures).",
    authors: [Devs._7n7],

    start() {
        const style = document.createElement("style");
        style.id = STYLE_ID;
        style.textContent = CSS;
        document.head.appendChild(style);
    },

    stop() {
        document.getElementById(STYLE_ID)?.remove();
    },
});
